/// aligns better with industry standards.
public typealias TokenUsage = Usage

// MARK: - Codable Conformance

extension Usage: Codable {
    
    /// Custom encoding to handle the Any dictionary properly
    public func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        
        try container.encode(promptTokens, forKey: .promptTokens)
        try container.encode(completionTokens, forKey: .completionTokens)
        try container.encode(totalTokens, forKey: .totalTokens)
        try container.encodeIfPresent(promptCost, forKey: .promptCost)
        try container.encodeIfPresent(completionCost, forKey: .completionCost)
        try container.encodeIfPresent(totalCost, forKey: .totalCost)
        try container.encodeIfPresent(currency, forKey: .currency)
        
        // Note: details dictionary is properly encoded as [String: String]
        // Providers should include specific details in their own response types
    }
    
    /// Custom decoding that ignores the details field
    public init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        
        promptTokens = try container.decode(Int.self, forKey: .promptTokens)
        completionTokens = try container.decode(Int.self, forKey: .completionTokens)
        totalTokens = try container.decode(Int.self, forKey: .totalTokens)
        promptCost = try container.decodeIfPresent(Double.self, forKey: .promptCost)
        completionCost = try container.decodeIfPresent(Double.self, forKey: .completionCost)
        totalCost = try container.decodeIfPresent(Double.self, forKey: .totalCost)
        currency = try container.decodeIfPresent(String.self, forKey: .currency)
        
        // details is not decoded and remains nil
        details = nil
    }
    
    private enum CodingKeys: String, CodingKey {
        case promptTokens = "prompt_tokens"
        case completionTokens = "completion_tokens"
        case totalTokens = "total_tokens"
        case promptCost = "prompt_cost"
        case completionCost = "completion_cost"
        case totalCost = "total_cost"
        case currency
    }
}

// MARK: - Equatable Conformance

extension Usage: Equatable {
    
    /// Compare two Usage instances for equality.
    ///
    /// Note: The details dictionary is compared properly since it contains [String: String]
    /// which is not Equatable. Only the token counts and cost information are compared.
    public static func == (lhs: Usage, rhs: Usage) -> Bool {
        return lhs.promptTokens == rhs.promptTokens &&
               lhs.completionTokens == rhs.completionTokens &&
               lhs.totalTokens == rhs.totalTokens &&
               lhs.promptCost == rhs.promptCost &&
               lhs.completionCost == rhs.completionCost &&
               lhs.totalCost == rhs.totalCost &&
               lhs.currency == rhs.currency
    }
}

// MARK: - Hashable Conformance

extension Usage: Hashable {
    
    /// Hash this Usage instance.
    ///
    /// Note: The details dictionary is not included in the hash since it contains
    /// [String: String] which is Hashable.
    public func hash(into hasher: inout Hasher) {
        hasher.combine(promptTokens)
        hasher.combine(completionTokens)
        hasher.combine(totalTokens)
        hasher.combine(promptCost)
        hasher.combine(completionCost)
        hasher.combine(totalCost)
        hasher.combine(currency)
    }
}